function fmtFeature(v) {
  return typeof v === 'number' ? v.toFixed(3).padStart(7) : String(v).padStart(7);
}

function main() {
  const painting = paintingFeatures();
  if (!painting.ok) {
    console.error('Could not load painting features:', painting.error);
    process.exit(1);
  }

  console.log('Painting features (the reference target):');
  for (const k of Object.keys(FEATURE_WEIGHTS)) {
    console.log(`  ${k.padEnd(16)} ${fmtFeature(painting[k])}   (weight ${FEATURE_WEIGHTS[k]}, tolerance ${FEATURE_TOLERANCE[k]})`);
  }
  console.log();

  // Header
  const cols = ['shape', 'latSpread', 'descCols', 'maxDepth', 'soilDisp', 'soilLogR', 'compositeScore'];
  console.log(cols.map(c => c.padStart(c === 'shape' ? 28 : 14)).join(''));
  console.log('-'.repeat(28 + 14 * (cols.length - 1)));

  const rows = [];
  for (const s of shapes) {
    const f = extractFeatures(s.ascii);
    if (!f.ok) {
      console.log(`  ${s.id} ${s.name}: extraction failed — ${f.error}`);
      continue;
    }
    const sc = shapeScore(f, painting);
    const row = [
      `${s.id} ${s.name}`.padStart(28),
      fmtFeature(f.lateralSpread).padStart(14),
      fmtFeature(f.descentColumns).padStart(14),
      fmtFeature(f.maxDepth).padStart(14),
      fmtFeature(f.soilDispersion).padStart(14),
      fmtFeature(f.soilLogRatio).padStart(14),
      fmtFeature(sc.score).padStart(14),
    ];
    console.log(row.join(''));
    rows.push({ shape: s, features: f, score: sc });
  }

  console.log();
  console.log('Per-feature breakdown of composite (run × target → featureScore × weight):');
  for (const r of rows) {
    console.log(`\n  ${r.shape.id} ${r.shape.name}  → composite ${r.score.score.toFixed(3)}`);
    for (const k of Object.keys(FEATURE_WEIGHTS)) {
      const p = r.score.perFeature[k];
      const contrib = p.score * FEATURE_WEIGHTS[k];
      console.log(`    ${k.padEnd(16)} run=${fmtFeature(p.run)}  target=${fmtFeature(p.target)}  fScore=${p.score.toFixed(3)}  ×w=${FEATURE_WEIGHTS[k]}  →  ${contrib.toFixed(3)}`);
    }
  }
}

main();
